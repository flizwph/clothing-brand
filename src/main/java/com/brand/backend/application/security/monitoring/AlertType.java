package com.brand.backend.application.security.monitoring;

/**
 * Типы оповещений безопасности
 */
public enum AlertType {
    /**
     * Обнаружена возможная атака методом перебора (брутфорс)
     */
    BRUTE_FORCE_ATTEMPT,
    
    /**
     * Обнаружена подозрительная активность с одного IP-адреса
     */
    SUSPICIOUS_IP_ACTIVITY,
    
    /**
     * Обнаружена подозрительная активность для одного пользователя
     */
    SUSPICIOUS_USER_ACTIVITY,
    
    /**
     * Обнаружена попытка доступа из необычного местоположения
     */
    UNUSUAL_LOCATION,
    
    /**
     * Обнаружена подозрительная активность с правами администратора
     */
    ADMIN_ACTIVITY,
    
    /**
     * Обнаружена атака с использованием скомпрометированных учётных данных
     */
    CREDENTIAL_STUFFING,
    
    /**
     * Обнаружен аномальный шаблон поведения пользователя
     */
    ANOMALOUS_USER_BEHAVIOR
} 